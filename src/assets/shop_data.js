const genders = products => [...new Set(products.map(product => product.gender.toLowerCase()))]
const categories = products => [...new Set(products.map(product => product.category.toLowerCase()))]
const brands = products => [...new Set(products.map(product => product.brand.toLowerCase()))]
const sizes = products => [...new Set(products.map(product => product.sizes).flat(1))]
const colors = products => [...new Set(products.map(product => product.colors.map(productColor => productColor.color)).flat(1))]

const getFilters = filters => filters.map(filter => ({ name: filter, isChecked: false }))

export const filters = (products) => ({
    gender: getFilters(genders(products)),
    category: getFilters(categories(products)),
    brand: getFilters(brands(products)),
    size: getFilters(sizes(products)),
    color: getFilters(colors(products)),
})

